def _coerce_str(value: Any) -> str | None:
    if not isinstance(value, str):
        return None
    trimmed = value.strip()
    return trimmed or None


def _collect_input_images(source: dict[str, Any]) -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    # Pre-normalized from multipart path
    raw = source.get("images")
    if isinstance(raw, list):
        for entry in raw:
            if isinstance(entry, dict) and entry.get("type") == "input_image":
                out.append(entry)
            else:
                normalized = _normalize_loose_image_value(entry)
                out.extend(normalized)
    # Loose JSON fields (e.g. body has {"image": "https://..."})
    for key in ("image",):
        value = source.get(key)
        if value is None:
            continue
        out.extend(_normalize_loose_image_value(value))
    return out


def _collect_mask_image(source: dict[str, Any]) -> dict[str, Any] | None:
    raw = source.get("mask")
    if raw is None:
        return None
    if isinstance(raw, dict) and raw.get("type") == "input_image":
        return raw
    normalized = _normalize_loose_image_value(raw)
    return normalized[0] if normalized else None


def _normalize_loose_image_value(value: Any) -> list[dict[str, Any]]:
    if isinstance(value, list):
        out: list[dict[str, Any]] = []
        for item in value:
            out.extend(_normalize_loose_image_value(item))
        return out
    if isinstance(value, str):
        url = value.strip()
        if not url:
            return []
        return [{"type": "input_image", "image_url": url}]
    if isinstance(value, dict):
        if "file_id" in value and value["file_id"]:
            return [{"type": "input_image", "file_id": value["file_id"]}]
        url = value.get("image_url") or value.get("url")
        if isinstance(url, str) and url.strip():
            return [{"type": "input_image", "image_url": url.strip()}]
        b64 = value.get("b64_json")
        if isinstance(b64, str) and b64:
            mime = value.get("mime_type") or "image/png"
            return [
                {
                    "type": "input_image",
                    "image_url": f"data:{mime};base64,{b64}",
                }
            ]
    return []


def _build_mask_payload(mask: dict[str, Any]) -> dict[str, Any]:
    if "file_id" in mask and mask["file_id"]:
        return {"file_id": mask["file_id"]}
    if "image_url" in mask and mask["image_url"]:
        return {"image_url": mask["image_url"]}
    return dict(mask)


