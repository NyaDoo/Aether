_MULTIPART_BOUNDARY_RE = re.compile(
    r'boundary\s*=\s*"?([^";]+)"?', re.IGNORECASE
)


def parse_multipart_image_edit(
    raw_body: bytes,
    content_type: str,
) -> dict[str, Any] | None:
    """Parse a multipart/form-data body from /v1/images/edits.

    Returns a dict of normalized fields compatible with
    transform_image_request_to_responses (operation="edit"):

        {
            "model": str,
            "prompt": str,
            "n": int | None,
            "size": str | None,
            "response_format": "b64_json",
            "user": str | None,
            "images": [{"type": "input_image", "image_url": "data:..;base64,.."}, ...],
            "mask": {"type": "input_image", ...} | None,
        }

    Returns None if the body cannot be parsed (missing boundary, malformed
    data, etc.).
    """
    if not raw_body or not content_type:
        return None
    match = _MULTIPART_BOUNDARY_RE.search(content_type)
    if not match:
        return None
    boundary = match.group(1).encode("ascii", errors="ignore")
    if not boundary:
        return None

    fields = list(_iter_multipart_fields(raw_body, boundary))
    if not fields:
        return None

    out: dict[str, Any] = {
        "images": [],
        "mask": None,
    }

    for field in fields:
        name = field["name"].strip()
        if not name:
            continue
        if name in ("image", "image[]", "images[]"):
            entry = _bytes_to_input_image(field["data"], field.get("content_type"))
            if entry is not None:
                out["images"].append(entry)
            continue
        if name == "mask":
            out["mask"] = _bytes_to_input_image(field["data"], field.get("content_type"))
            continue
        # Text fields
        try:
            text_value = field["data"].decode("utf-8", errors="replace").strip()
        except Exception:
            continue
        if name in ("n",):
            try:
                out[name] = int(text_value)
            except Exception:
                pass
        else:
            out[name] = text_value

    return out


def _iter_multipart_fields(body: bytes, boundary: bytes):
    """Minimal multipart/form-data parser — assumes CRLF line endings.

    Does NOT support chunked bodies or pathological malformed input; callers
    should wrap exceptions. Good enough for well-formed OpenAI SDK clients.
    """
    delim = b"--" + boundary
    end_delim = delim + b"--"

    # Split into parts
    chunks = body.split(delim)
    for raw in chunks:
        raw = raw.strip(b"\r\n")
        if not raw or raw == b"--":
            continue
        if raw.startswith(b"--"):
            continue
        # Each chunk = headers \r\n\r\n body
        sep = raw.find(b"\r\n\r\n")
        if sep < 0:
            continue
        header_block = raw[:sep].decode("latin-1", errors="ignore")
        data = raw[sep + 4 :]
        # Strip trailing CRLF that precedes next boundary
        if data.endswith(b"\r\n"):
            data = data[:-2]

        field: dict[str, Any] = {
            "name": "",
            "filename": None,
            "content_type": None,
            "data": data,
        }
        for line in header_block.split("\r\n"):
            if not line:
                continue
            lower = line.lower()
            if lower.startswith("content-disposition:"):
                name_match = re.search(r'name="([^"]+)"', line)
                if name_match:
                    field["name"] = name_match.group(1)
                filename_match = re.search(r'filename="([^"]*)"', line)
                if filename_match:
                    field["filename"] = filename_match.group(1)
            elif lower.startswith("content-type:"):
                field["content_type"] = line.split(":", 1)[1].strip()
        if field["name"]:
            yield field

    # Consume end_delim just to satisfy linter — actual split already handled it
    _ = end_delim


def _bytes_to_input_image(
    data: bytes,
    content_type: str | None,
) -> dict[str, Any] | None:
    if not data:
        return None
    mime = (content_type or "").strip() or "application/octet-stream"
    b64 = base64.standard_b64encode(data).decode("ascii")
    return {
        "type": "input_image",
        "image_url": f"data:{mime};base64,{b64}",
    }


